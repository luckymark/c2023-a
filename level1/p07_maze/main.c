int main(){
    char list[11][11]={
            {'#','#','#','#','#','#','#','#','#','#','#'},
            {'#',' ',' ',' ',' ',' ',' ',' ',' ',' ','#'},
            {'#',' ','#','#','#',' ',' ','#',' ',' ','#'},
            {'#',' ',' ',' ',' ','#',' ',' ','#',' ','#'},
            {'#','#','#','#',' ',' ','#',' ',' ',' ','#'},
            {'#',' ',' ',' ',' ',' ',' ',' ','#','#','#'},
            {'#',' ','#','#','#','#','#','#','#',' ','#'},
            {'#',' ','#',' ',' ',' ','#',' ',' ',' ','#'},
            {'#',' ','#',' ','#',' ','#',' ','#',' ','#'},
            {'#',' ',' ',' ','#',' ',' ',' ','#',' ','#'},
            {'#','#','#','#','#','#','#','#','#','^','#'}
    };
    int i,j,f=1;
    int ip=1,jp=1;
    char ch;
    char name='P';
    while(f==1) {
        system("cls");
        for (i = 0; i < 11; i++) {
            for (j = 0; j < 11; j++) {
                if (i==ip && j==jp)
                {
                    printf("%c",name);
                }
                else
                {
                    printf("%c", list[i][j]);
                }
            }
            printf("\n");
        }
        ch=getch();
        if ((ch=='w') && (list[ip-1][jp]!='#'))
        {
            ip--;
        }
        else if ((ch=='s') && (list[ip+1][jp]!='#'))
        {
            ip++;
        }
        else if ((ch=='a') && (list[ip][jp-1]!='#'))
        {
            jp--;
        }
        else if ((ch=='d') && (list[ip][jp+1]!='#'))
        {
            jp++;
        }
        if(list[ip][jp]=='^')
        {
            f=0;
        }
    }
    system("cls");
    printf("³É¹¦");
    system("pause");
    return 0;
}